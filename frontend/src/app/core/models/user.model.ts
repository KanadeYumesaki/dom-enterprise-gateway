/**
 * ユーザー情報のインターフェース
 * バックエンドの AuthenticatedUser スキーマに対応しています。
 * 
 * 主な用途:
 * - ログイン中のユーザー情報を保持する
 * - 権限チェック（管理者かどうか）を行う
 */
export interface User {
    /** ユーザーの一意なID (UUID形式など) */
    id: string;

    /** ユーザーのメールアドレス */
    email: string;

    /** 表示名（任意）。設定されていない場合はメールアドレスなどを表示に使います。 */
    name?: string;

    /**
     * ユーザーが持つ権限（ロール）のリスト
     * - 'admin': システム管理者。全データにアクセス可能。
     * - 'user': 一般ユーザー。自テナントのデータのみアクセス可能。
     */
    roles: ('admin' | 'user')[];

    /** 所属するテナント（組織）のID */
    tenant_id: string;

    /** プロフィール画像のURL（任意） */
    picture_url?: string;
}
